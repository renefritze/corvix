"""Contract tests binding the backend API to its generated schema.

These guard the schema contract described in issue F7:

* the committed OpenAPI document (``frontend/openapi.json``) stays in sync with
  the Litestar app's auto-generated schema, so a backend field change that is
  not regenerated fails CI rather than silently breaking the frontend;
* the actual JSON the handlers emit conforms to the typed response schemas in
  ``corvix.web.schemas`` (every declared field present, correctly typed, and no
  undeclared extras).

The TypeScript side of the contract (``frontend/src/api-types.gen.ts``) is
drift-checked in CI via ``make gen-types`` + ``git diff --exit-code``.
"""

from __future__ import annotations

import dataclasses
import importlib.util
import json
from collections.abc import Generator
from pathlib import Path
from types import ModuleType

import msgspec
import pytest
from litestar.testing import TestClient

from corvix.config import load_config
from corvix.web.app import app
from corvix.web.schemas import RuleSnippetsResponse, SnapshotResponse
from corvix.web.storage_provider import set_storage_backend
from tests.support.storage import JsonFileStorage

_REPO_ROOT = Path(__file__).resolve().parents[2]
_OPENAPI_PATH = _REPO_ROOT / "frontend" / "openapi.json"
_GENERATED_TS_PATH = _REPO_ROOT / "frontend" / "src" / "api-types.gen.ts"


def _load_export_module() -> ModuleType:
    """Import ``scripts/export_openapi.py`` as a module (it is not a package)."""
    spec = importlib.util.spec_from_file_location(
        "corvix_export_openapi",
        _REPO_ROOT / "scripts" / "export_openapi.py",
    )
    assert spec is not None
    assert spec.loader is not None
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


def test_committed_openapi_matches_app_schema() -> None:
    """The committed OpenAPI doc must match what the app generates now."""
    export = _load_export_module()
    rendered = export.render_openapi()
    committed = json.loads(_OPENAPI_PATH.read_text(encoding="utf-8"))
    assert rendered == committed, (
        "frontend/openapi.json is stale. Regenerate it and the TypeScript types "
        "with `make gen-types` and commit the result."
    )


def test_generated_typescript_exists_and_is_marked_generated() -> None:
    """The generated TS types are committed and clearly marked auto-generated."""
    assert _GENERATED_TS_PATH.is_file(), "Run `make gen-types` to generate frontend/src/api-types.gen.ts."
    header = _GENERATED_TS_PATH.read_text(encoding="utf-8")[:200]
    assert "auto-generated by openapi-typescript" in header


