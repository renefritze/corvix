def _assert_conforms(body: bytes, schema: type) -> None:
    """Decode *body* into *schema*, asserting required fields and types match.

    msgspec raises if a required field is missing or mistyped; we additionally
    assert the top-level keys match the schema's fields exactly so the backend
    cannot emit undeclared extras that the OpenAPI contract does not describe.
    """
    decoded = msgspec.json.decode(body, type=schema)
    assert isinstance(decoded, schema)
    expected_keys = {field.name for field in dataclasses.fields(schema)}
    actual_keys = set(json.loads(body))
    assert actual_keys == expected_keys


def test_snapshot_response_conforms_to_schema(contract_client: TestClient) -> None:
    response = contract_client.get("/api/v1/snapshot?dashboard=overview")
    assert response.status_code == 200
    _assert_conforms(response.content, SnapshotResponse)


def test_rule_snippets_response_conforms_to_schema(contract_client: TestClient) -> None:
    response = contract_client.get("/api/v1/notifications/primary/101/rule-snippets?dashboard=overview")
